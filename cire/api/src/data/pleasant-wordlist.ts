/**
 * The exported word bank: de-duplicated (first occurrence wins), lowercase,
 * 3–10 chars, frozen. The minimum size is asserted so an accidental shrink can
 * never silently weaken the WORD segment below its intended entropy.
 */
const deduped = Array.from(new Set(SEED));

const WORD_RE = /^[a-z]{3,10}$/;
const bad = deduped.find((w) => !WORD_RE.test(w));
if (bad) {
  // Programmer error: a malformed word slipped into the seed.
  throw new Error(`pleasant-wordlist: invalid word "${bad}" (must match ${WORD_RE})`);
}

/** The entropy floor for this bank — fewer than this and codes get too guessable. */
export const MIN_WORDS = 300;

if (deduped.length < MIN_WORDS) {
  throw new Error(
    `pleasant-wordlist: only ${deduped.length} unique words available, need ${MIN_WORDS}`,
  );
}

/** Curated, de-duplicated, lowercase pleasant words (3–10 chars each). */
export const PLEASANT_WORDS: readonly string[] = Object.freeze(deduped);

/** Alias kept so `family-code.ts` can import a stable `WORDLIST` name. */
export const WORDLIST = PLEASANT_WORDS;
